class DeepfakeDataset(Dataset):
    def __init__(self, files, labels, audio_condition='clean'):
        self.samples = []
        for f, l in zip(files, labels):
            name = f.replace('.mp4', '')
            audio_path = f'{DATA_DIR}/audio/{name}.pt'
            frames_path = f'{DATA_DIR}/frames/{name}.pt'
            if os.path.exists(audio_path) and os.path.exists(frames_path):
                self.samples.append((name, l))

        self.audio_condition = audio_condition
        print(f"Dataset: {len(self.samples)} samples | condition: {audio_condition}")

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        name, label = self.samples[idx]

        # ---- Frames ----
        frames = torch.load(f'{DATA_DIR}/frames/{name}.pt')
        frames = frames.float() / 255.0

        if frames.shape[0] < 4:
            pad = torch.zeros(4 - frames.shape[0], *frames.shape[1:])
            frames = torch.cat([frames, pad], dim=0)
        else:
            frames = frames[:4]

        # ---- Audio ----
        audio = torch.load(f'{DATA_DIR}/audio/{name}.pt')

        target_len = 48000
        if audio.shape[1] >= target_len:
            audio = audio[:, :target_len]
        else:
            pad = torch.zeros(1, target_len - audio.shape[1])
            audio = torch.cat([audio, pad], dim=1)

        # Normalize audio
        audio = audio / (audio.abs().max() + 1e-8)

        condition = self.audio_condition

        # ---- Mixed training ----
        if condition == 'mixed':
            choice = random.choice(['clean', 'noisy', 'missing'])

            if choice == 'noisy':
                noise = torch.randn_like(audio) * 1.0
                audio = audio + noise

            elif choice == 'missing':
                audio = torch.zeros_like(audio) + 1e-6

        elif condition == 'noisy':
            noise = torch.randn_like(audio) * 1.0
            audio = audio + noise

        elif condition == 'missing':
            audio = torch.zeros_like(audio) + 1e-6

        return frames, audio, torch.tensor(label, dtype=torch.float32)


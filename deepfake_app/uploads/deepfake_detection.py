def get_optimal_threshold(model, val_loader, device):
    model.eval()
    all_probs, all_labels = [], []
    with torch.no_grad():
        for frames, audio, labels in val_loader:
            frames, audio = frames.to(device), audio.to(device)
            preds, _ = model(frames, audio)
            probs = torch.sigmoid(preds)
            all_probs.extend(probs.cpu().numpy())
            all_labels.extend(labels.cpu().numpy())

    fpr, tpr, thresholds = roc_curve(all_labels, all_probs)
    j_scores = tpr - fpr
    best_threshold = thresholds[np.argmax(j_scores)]
    return best_threshold


def test_model(condition, threshold):
    print(f"Testing Condition: {condition} (Threshold: {threshold:.4f})")

    test_ds = DeepfakeDataset(test_files, test_labels, audio_condition=condition)
    test_loader = DataLoader(test_ds, batch_size=4, shuffle=False)

    model.eval()
    all_probs, all_labels = [], []

    with torch.no_grad():
        for frames, audio, labels in test_loader:
            frames, audio = frames.to(device), audio.to(device)
            preds, _ = model(frames, audio)
            probs = torch.sigmoid(preds)
            all_probs.extend(probs.cpu().numpy())
            all_labels.extend(labels.cpu().numpy())

    all_probs = np.array(all_probs)
    all_labels = np.array(all_labels)
    all_preds = (all_probs > threshold).astype(int)

    metrics = {
        "acc": accuracy_score(all_labels, all_preds),
        "prec": precision_score(all_labels, all_preds, zero_division=0),
        "rec": recall_score(all_labels, all_preds, zero_division=0),
        "f1": f1_score(all_labels, all_preds, zero_division=0),
        "auc": roc_auc_score(all_labels, all_probs)
    }

    print(f"Accuracy: {metrics['acc']:.4f}, Precision: {metrics['prec']:.4f}, "
          f"Recall: {metrics['rec']:.4f}, F1: {metrics['f1']:.4f}, AUC: {metrics['auc']:.4f}")
    print("-" * 30)
    return metrics


